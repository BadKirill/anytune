#!/usr/bin/env node
/**
 * Regenerates the auto-managed inventory block inside file-index.md from the
 * filesystem. Manual prose outside the markers is preserved.
 */
import { execFileSync } from 'node:child_process'
import {
  mkdtempSync,
  readdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync,
} from 'node:fs'
import { tmpdir } from 'node:os'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const TARGET = join(ROOT, 'docs', 'knowledge', 'areas', 'file-index.md')
const START = '<!-- AUTO:FILE-INDEX:START -->'
const END = '<!-- AUTO:FILE-INDEX:END -->'

const SKIP_DIR_NAMES = new Set([
  'node_modules',
  'dist',
  'dev-dist',
  'coverage',
  '.git',
  'android',
  'ios',
  'test-results',
  'playwright-report',
  'assets',
])

function walk(dir, out = []) {
  for (const name of readdirSync(dir)) {
    if (name === '.DS_Store') continue
    const abs = join(dir, name)
    const st = statSync(abs)
    if (st.isDirectory()) {
      if (SKIP_DIR_NAMES.has(name)) continue
      walk(abs, out)
    } else {
      out.push(abs)
    }
  }
  return out
}

function rel(abs) {
  return relative(ROOT, abs).split('\\').join('/')
}

function section(title, paths) {
  if (paths.length === 0) return ''
  const rows = paths.map((p) => `| \`${p}\` | |`).join('\n')
  return `### ${title}\n\n| Path | Notes |\n| ---- | ----- |\n${rows}\n`
}

function buildBlock() {
  const src = walk(join(ROOT, 'src'))
    .map(rel)
    .filter((p) => p.startsWith('src/'))
    .sort()
  const e2e = walk(join(ROOT, 'e2e')).map(rel).sort()
  const scripts = walk(join(ROOT, 'scripts')).map(rel).sort()
  const workflows = walk(join(ROOT, '.github', 'workflows'))
    .map(rel)
    .sort()
  const knowledge = walk(join(ROOT, 'docs', 'knowledge'))
    .map(rel)
    .sort()

  const config = [
    'package.json',
    'vite.config.ts',
    'playwright.config.ts',
    'eslint.config.js',
    'tsconfig.json',
    'tsconfig.app.json',
    'tsconfig.node.json',
    'index.html',
    'AGENTS.md',
    'CLAUDE.md',
    'llms.txt',
    'README.md',
    '.github/copilot-instructions.md',
  ].filter((p) => {
    try {
      statSync(join(ROOT, p))
      return true
    } catch {
      return false
    }
  })

  return [
    START,
    '',
    '_Generated by `npm run knowledge:refresh`. Do not edit between markers._',
    '',
    section('Source (`src/`)', src),
    section('E2E', e2e),
    section('Scripts', scripts),
    section('Workflows', workflows),
    section('Knowledge tree', knowledge),
    section('Root / agent entry', config),
    END,
  ].join('\n')
}

function applyBlock(current, block) {
  if (current.includes(START) && current.includes(END)) {
    return current.replace(new RegExp(`${START}[\\s\\S]*?${END}`), block)
  }
  return `${current.trimEnd()}\n\n## Auto inventory\n\n${block}\n`
}

function prettierFormat(contents) {
  const dir = mkdtempSync(join(tmpdir(), 'anytune-file-index-'))
  const tmp = join(dir, 'file-index.md')
  try {
    writeFileSync(tmp, contents)
    execFileSync('npx', ['prettier', '--write', tmp], {
      cwd: ROOT,
      stdio: ['ignore', 'ignore', 'pipe'],
    })
    return readFileSync(tmp, 'utf8')
  } finally {
    rmSync(dir, { recursive: true, force: true })
  }
}

function main() {
  const checkOnly = process.argv.includes('--check')
  const current = readFileSync(TARGET, 'utf8')
  const next = prettierFormat(applyBlock(current, buildBlock()))
  if (next === current) {
    console.log('Auto inventory already up to date')
    return
  }
  if (checkOnly) {
    console.error(
      'file-index.md auto inventory is stale — run: npm run knowledge:refresh',
    )
    process.exit(1)
  }
  writeFileSync(TARGET, next)
  console.log('Updated auto inventory in docs/knowledge/areas/file-index.md')
}

main()
